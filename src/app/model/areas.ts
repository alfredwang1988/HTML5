export function findProvivceByCountry(country) {
  let result = null;
  let flag = true;
  NodeMap.forEach((item) => {
    if (country === item.en && flag) {
      result = item.provinces;
      flag = false;
    }
  });
  return result;
}

export function findCityByProvince(province_p) {
  let result = null;
  NodeMap.forEach((country) => {
    country.provinces.forEach((province) => {
      if (province.zh === province_p) {
        result = province.cities;
        return false;
      }
    });
  });
  return result;
}
